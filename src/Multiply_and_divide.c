#include <stdio.h>
#include "../include/Multiply_and_divide.h"


int Multiply() {

    int zahl1;
    int zahl2;
    scanf("%d %d", &zahl1, &zahl2);
    printf("%d\n", zahl1 * zahl2);
    return 0;
}

int Divide() {

    int zahl1;
    int zahl2;
    scanf("%d %d", &zahl1, &zahl2);
    printf("%d\n", zahl1 / zahl2);
    return 0;
}

